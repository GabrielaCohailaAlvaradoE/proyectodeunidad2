package modelo;

import java.util.Date;

public class HistorialEntry {

    private int idRegistro;
    private String placaVehiculo;
    private String marcaModeloVehiculo;
    private Date horaEntrada;
    private Date horaSalida;
    private double montoPagado;
    private Date fechaPago; // Viene de tb_pagos

    public HistorialEntry() {
    }

    // --- Getters y Setters ---

    public int getIdRegistro() {
        return idRegistro;
    }

    public void setIdRegistro(int idRegistro) {
        this.idRegistro = idRegistro;
    }

    public String getPlacaVehiculo() {
        return placaVehiculo;
    }

    public void setPlacaVehiculo(String placaVehiculo) {
        this.placaVehiculo = placaVehiculo;
    }

    public String getMarcaModeloVehiculo() {
        return marcaModeloVehiculo;
    }

    public void setMarcaModeloVehiculo(String marcaModeloVehiculo) {
        this.marcaModeloVehiculo = marcaModeloVehiculo;
    }

    public Date getHoraEntrada() {
        return horaEntrada;
    }

    public void setHoraEntrada(Date horaEntrada) {
        this.horaEntrada = horaEntrada;
    }

    public Date getHoraSalida() {
        return horaSalida;
    }

    public void setHoraSalida(Date horaSalida) {
        this.horaSalida = horaSalida;
    }

    public double getMontoPagado() {
        return montoPagado;
    }

    public void setMontoPagado(double montoPagado) {
        this.montoPagado = montoPagado;
    }

    public Date getFechaPago() {
        return fechaPago;
    }

    public void setFechaPago(Date fechaPago) {
        this.fechaPago = fechaPago;
    }
}